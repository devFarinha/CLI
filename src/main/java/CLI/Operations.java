package CLI;

public class Operations
{
    public static int sum(int a, int b)
    {
        int result = a+b;
        return result;
    }

    public static int substract(int a, int b)
    {
        int result = a-b;
        return result;
    }

    public static double divide(double a, double b)
    {
        double result = a/b;
        return result;
    }

    public static int multiplicate(int a, int b)
    {
        int result = a*b;
        return result;
    }
}
